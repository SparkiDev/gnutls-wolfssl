static int test_hash(gnutls_digest_algorithm_t hash_alg,
    const unsigned char* buf, size_t buf_sz, unsigned char* output,
    size_t output_sz, const unsigned char* expected_hash,
    const unsigned char* expected_long_hash)
{
    int ret;
    gnutls_hash_hd_t hash;
    int i;

    /* Rest of test code remains the same */
    printf("Testing wolfSSL's %s implementation via GnuTLS...\n",
        gnutls_digest_get_name(hash_alg));
    printf("Input data: \"%s\"\n", test_data);

    ret = gnutls_hash_init(&hash, hash_alg);
    if (ret != 0) {
        print_gnutls_error("initializing hash", ret);
        return 1;
    }

    ret = gnutls_hash(hash, test_data, strlen(test_data));
    if (ret != 0) {
        print_gnutls_error("hashing the data", ret);
        gnutls_hash_deinit(hash, NULL);
        return 1;
    }

    gnutls_hash_deinit(hash, output);

    if (compare("Hash", output, expected_hash, output_sz) != 0) {
        return 1;
    }

    ret = gnutls_hash_init(&hash, hash_alg);
    if (ret != 0) {
        print_gnutls_error("initializing hash", ret);
        return 1;
    }

    for (i = 0; i < 10; i++) {
        ret = gnutls_hash(hash, buf, buf_sz);
        if (ret != 0) {
            print_gnutls_error("hashing the data", ret);
            gnutls_hash_deinit(hash, NULL);
            return 1;
        }
    }

    gnutls_hash_deinit(hash, output);

    if (compare("Long hash", output, expected_long_hash, output_sz) != 0) {
        return 1;
    }

    return 0;
}

int main(void)
{
    int ret;
    unsigned char buf[256];
    unsigned char output[64];

    memset(buf, 0xa5, sizeof(buf));

    /* Initialize GnuTLS */
    ret = gnutls_global_init();
    if (ret != 0) {
        print_gnutls_error("initializing GnuTLS", ret);
        return 1;
    }

    ret = test_hash(GNUTLS_DIG_SHA224, buf, sizeof(buf), output, 28,
        expected_hash_sha224, expected_long_hash_sha224);
    if (ret == 0) {
        ret = test_hash(GNUTLS_DIG_SHA256, buf, sizeof(buf), output, 32,
            expected_hash_sha256, expected_long_hash_sha256);
    }
    if (ret == 0) {
        ret = test_hash(GNUTLS_DIG_SHA384, buf, sizeof(buf), output, 48,
            expected_hash_sha384, expected_long_hash_sha384);
    }
    if (ret == 0) {
        ret = test_hash(GNUTLS_DIG_SHA512, buf, sizeof(buf), output, 64,
            expected_hash_sha512, expected_long_hash_sha512);
    }
    if (ret == 0) {
        ret = test_hash(GNUTLS_DIG_SHA3_224, buf, sizeof(buf), output, 28,
            expected_hash_sha3_224, expected_long_hash_sha3_224);
    }
    if (ret == 0) {
        ret = test_hash(GNUTLS_DIG_SHA3_256, buf, sizeof(buf), output, 32,
            expected_hash_sha3_256, expected_long_hash_sha3_256);
    }
    if (ret == 0) {
        ret = test_hash(GNUTLS_DIG_SHA3_384, buf, sizeof(buf), output, 48,
            expected_hash_sha3_384, expected_long_hash_sha3_384);
    }
    if (ret == 0) {
        ret = test_hash(GNUTLS_DIG_SHA3_512, buf, sizeof(buf), output, 64,
            expected_hash_sha3_512, expected_long_hash_sha3_512);
    }
    if (ret == 0) {
        printf("Test completed.\n");
    }

    gnutls_global_deinit();

    return ret;
}

