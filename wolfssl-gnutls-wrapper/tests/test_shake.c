static int test_one_block(int algorithm, int len,
    const unsigned char* expected_hash)
{
    int ret;
    gnutls_hash_hd_t hash;
    unsigned char output[168];


    printf("Testing wolfSSL's SHAKLE implementation via GnuTLS...\n");
    printf("Input data: \"%s\"\n", test_data);

    ret = gnutls_hash_init(&hash, algorithm);
    if (ret != 0) {
        print_gnutls_error("initializing hash", ret);
        return 1;
    }

    ret = gnutls_hash(hash, test_data, strlen(test_data));
    if (ret != 0) {
        print_gnutls_error("hashing the data", ret);
        gnutls_hash_deinit(hash, NULL);
        return 1;
    }

    ret = gnutls_hash_squeeze(hash, output, len);
    if (ret != 0) {
        print_gnutls_error("squeezing", ret);
        gnutls_hash_deinit(hash, NULL);
        return 1;
    }

    gnutls_hash_deinit(hash, NULL);

    if (compare("SHAKE", output, expected_hash, len) != 0) {
        return 1;
    }

    return 0;
}

static int test_stream_output(int algorithm, int len,
    const unsigned char* expected_hash)
{
    int ret;
    gnutls_hash_hd_t hash;
    int i;

    printf("Testing wolfSSL's SHAKLE implementation via GnuTLS...\n");
    printf("Input data: \"%s\"\n", test_data);

    ret = gnutls_hash_init(&hash, algorithm);
    if (ret != 0) {
        print_gnutls_error("initializing hash", ret);
        return 1;
    }

    ret = gnutls_hash(hash, test_data, strlen(test_data));
    if (ret != 0) {
        print_gnutls_error("hashing the data", ret);
        gnutls_hash_deinit(hash, NULL);
        return 1;
    }

    for (i = 0; i < 8; i++) {
        unsigned char output[32];

        ret = gnutls_hash_squeeze(hash, output, sizeof(output));
        if (ret != 0) {
            print_gnutls_error("squeezing", ret);
            gnutls_hash_deinit(hash, NULL);
            return 1;
        }

        if (compare("SHAKE", output, expected_hash + i * 32, 32) != 0) {
            gnutls_hash_deinit(hash, NULL);
            return 1;
        }
    }

    gnutls_hash_deinit(hash, NULL);

    return 0;
}

static int test_shake(int algorithm, int len,
    const unsigned char* expected_hash)
{
    int ret;

    ret = test_one_block(algorithm, len, expected_hash);
    if (ret == 0) {
        ret = test_stream_output(algorithm, len, expected_hash);
    }

    return ret;
}

int main(void)
{
    int ret;

    /* Initialize GnuTLS */
    ret = gnutls_global_init();
    if (ret != 0) {
        print_gnutls_error("initializing GnuTLS", ret);
        return 1;
    }

    printf("SHAKE-128\n");
    ret = test_shake(GNUTLS_DIG_SHAKE_128, 168, expected_hash_shake128);
    if (ret == 0) {
        printf("SHAKE-256\n");
        ret = test_shake(GNUTLS_DIG_SHAKE_256, 144, expected_hash_shake256);
    }

    gnutls_global_deinit();

    return ret;
}
