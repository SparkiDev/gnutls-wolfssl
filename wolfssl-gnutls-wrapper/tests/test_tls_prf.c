static int test_tls_prf(gnutls_mac_algorithm_t mac_alg,
    const unsigned char* expected, size_t sz)
{
    int ret;
    char output[64];

    /* Rest of test code remains the same */
    printf("Testing wolfSSL's %s TLS PRF implementation via GnuTLS...\n",
        gnutls_mac_get_name(mac_alg));

    ret = gnutls_prf_gen(mac_alg, sizeof(pre_master_secret),
        pre_master_secret, sizeof(label), label, sizeof(hello_random),
        hello_random, sz, output);
    if (ret != 0) {
        print_gnutls_error("calculating master secret", ret);
        return 1;
    }

    if (compare("Master", output, expected, sz) != 0) {
        return 1;
    }

    return 0;
}

int main(void)
{
    int ret;
    unsigned char buf[256];
    unsigned char output[64];

    memset(buf, 0xa5, sizeof(buf));

    /* Initialize GnuTLS */
    ret = gnutls_global_init();
    if (ret != 0) {
        print_gnutls_error("initializing GnuTLS", ret);
        return 1;
    }

    ret = test_tls_prf(GNUTLS_MAC_MD5_SHA1, expected_md5_sha1,
        sizeof(expected_md5_sha1));
    if (ret == 0) {
        ret = test_tls_prf(GNUTLS_MAC_SHA256, expected_sha256,
            sizeof(expected_sha256));
    }
    if (ret == 0) {
        ret = test_tls_prf(GNUTLS_MAC_SHA384, expected_sha384,
            sizeof(expected_sha384));
    }
    if (ret == 0) {
        printf("Test completed.\n");
    }

    gnutls_global_deinit();

    return ret;
}

